
public class Address {
	
	String street;
	String flatNo;
	String dist;
	String state;
	String Country;
	public Address(String street, String flatNo, String dist, String state, String country) {
		super();
		this.street = street;
		this.flatNo = flatNo;
		this.dist = dist;
		this.state = state;
		Country = country;
	}
	
	
	

}
